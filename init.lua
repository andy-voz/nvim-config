-- Set leader key to Space
vim.g.mapleader = " "

-- Enable line numbers
vim.opt.number = true

-- Enable syntax highlighting
vim.cmd("syntax enable")

-- Set tab width to 4 spaces
vim.opt.tabstop = 4
vim.opt.shiftwidth = 4
vim.opt.expandtab = true

-- Enable mouse support
vim.opt.mouse = "a"
